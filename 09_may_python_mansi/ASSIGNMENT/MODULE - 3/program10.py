''' Write a Python program to generate and print a list of first and last 5 
elements where the values are square of numbers between 1 and 30.'''

#  Calculates the square of 1,2,3,4,5 and assigns it to first_5_element(n).

first_5_element1 = 1**2
first_5_element2 = 2**2
first_5_element3 = 3**2
first_5_element4 = 4**2
first_5_element5 = 5**2

first_5 = [first_5_element1, first_5_element2, first_5_element3, first_5_element4, first_5_element5]

print("First 5 elements:", first_5)

##  Calculates the square of last 27,28,29,30 and assigns it to last_5_element(n).
last_5_element1 = 27**2
last_5_element2 = 28**2
last_5_element3 = 29**2
last_5_element4 = 30**2

last_5 = [last_5_element1, last_5_element2, last_5_element3, last_5_element4]

print("Last 5 elements:", last_5)



















































































